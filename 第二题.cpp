#include<stdio.h>
int main()
{
	float a=0;//奖金总数 
	int i=0;//利润
	printf("请输入利润：");
	scanf("%d",&i);
	if(i<=100000)
	{
		a=i*0.1;
	}
	if(i>100000&&i<200000)
	{
		a=10000+(i-100000)*0.075;
	}
	if(i>=200000&&i<400000)
	{
		a=10000+7500+(i-200000)*0.05;
	}
	if(i>=400000&&i<600000)
	{
		a=10000+7500+10000+(i-400000)*0.03;
	}
	if(i>=600000&&i<1000000)
	{
		a=10000+7500+10000+6000+(i-600000)*0.015;
	}
	if(i>=1000000)
	{
		a=10000+7500+10000+6000+6000+(i-1000000)*0.01;
	}
	printf("应发奖金数为%f",a);
	return 0;
}
